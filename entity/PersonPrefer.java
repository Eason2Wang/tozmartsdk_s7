package com.tozmart.tozisdk.entity;

public class PersonPrefer {
    private String ClothLength;
    private String ClothLoose;
    private String SleeveLength;
    private String PantsLoose;
    private String PantsLength;

    public String getClothLength() {
        return ClothLength;
    }

    public void setClothLength(String clothLength) {
        ClothLength = clothLength;
    }

    public String getClothLoose() {
        return ClothLoose;
    }

    public void setClothLoose(String clothLoose) {
        ClothLoose = clothLoose;
    }

    public String getSleeveLength() {
        return SleeveLength;
    }

    public void setSleeveLength(String sleeveLength) {
        SleeveLength = sleeveLength;
    }

    public String getPantsLoose() {
        return PantsLoose;
    }

    public void setPantsLoose(String pantsLoose) {
        PantsLoose = pantsLoose;
    }

    public String getPantsLength() {
        return PantsLength;
    }

    public void setPantsLength(String pantsLength) {
        PantsLength = pantsLength;
    }
}
